class TiposPrimitivos {
  public static void main(String[] args) {
    System.out.println("Hello world!");

    int i = 65;
    char c = (char) i;
    double d = 39.7;
    int x = (int) d;

    System.out.println("i: " + i);
    System.out.println("c: " + c);
    System.out.println("d: " + d);
    System.out.println("x: " + x);
  }
}
